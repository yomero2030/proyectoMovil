class User{
  int id;
  String name;
  String lastname;
  String phone;
  String address;
  String email;
  int user_id;

  User(int id, String name, String lastname, String phone, String address, String email, int user_id){
    this.id = id;
    this.name = name;
    this.lastname = lastname;
    this.phone = phone;
    this.address = address;
    this.email = email;
    this.user_id = user_id;
  }
}